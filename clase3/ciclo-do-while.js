'use strict'

//Bucle do while
//Ejecuta el bloque de instrucciones y luego evalua la condición
//Si la condición es verdadera se ejecuta nuevamente el bloque de instrucciones
//Si la condición es falsa se sale del bucle

var edad = 30

do {
  console.log('Solo cuando sea diferente a 20')
  edad--
} while (edad != 20)
